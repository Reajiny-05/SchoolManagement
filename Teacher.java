public class Teacher {
    String fullName;
    String teacherID;
    String instituteName;
    String email;
    String password;
    String phoneNumber;
    String studentScheduleID;
    String studentPortfolioID;
    
    public Teacher(String fullName, String teacherID, String instituteName, String email, String password,
            String phoneNumber, String studentScheduleID, String studentPortfolioID) {
        this.fullName = fullName;
        this.teacherID = teacherID;
        this.instituteName = instituteName;
        this.email = email;
        this.password = password;
        this.phoneNumber = phoneNumber;
        this.studentScheduleID = studentScheduleID;
        this.studentPortfolioID = studentPortfolioID;
    }

    
    
}
